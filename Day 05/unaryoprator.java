public class unaryoprator {
    public static void main(String[] args) {
        int G = 86;
        int S = -G;
        int GS = -S;
        System.out.println(GS);


        int SG = 72;
        SG = SG + 86;
    System.out.println(SG);

    int GS1 = 72;
    System.out.println(GS1++);
    System.out.println(GS1);

    int GS2 = 72;
    System.out.println(++GS2);
    System.out.println(GS2);

    }
}
