
public class Escape {

    public static void main(String[] args) {
        System.out.println("Hello  Doll");
        // Print value in double quotes
        System.out.println("Hello Doll \"love you\" ");
        // Print value in single quotes
        System.out.println("Hello Doll \'love you\' ");
        // Print value in backslash
        System.out.println("Hello Doll \\love you\\ ");
        // Print value in new line
        System.out.println("Hello Doll \nlove you ");
        // Print value in tab
        System.out.println("Hello Doll\tlove you ");
        // print value in backspace
        System.out.println("Hello Doll S\blove you ");

    }
}
